	enum {
		is_blank = 0x0001,	/* space \t */
		is_cntrl = 0x0002,	/* nonprinting characters */
		is_digit = 0x0004,	/* 0-9 */
		is_lower = 0x0008,	/* a-z */
		is_print = 0x0010,	/* any printing character including ' ' */
		is_punct = 0x0020,	/* all punctuation */
		is_space = 0x0040,	/* space \t \n \r \f \v */
		is_upper = 0x0080,	/* A-Z */
		is_xdigit = 0x0100,	/* 0-9A-Fa-f */
		is_idst = 0x0200,	/* A-Za-z_ */
		is_vsp = 0x0400,	/* \n \r */
		is_nvsp = 0x0800,	/* space \t \f \v \0 */
							/* Combinations of the above.  */
							is_alpha = is_upper | is_lower,	/* A-Za-z */
							is_alnum = is_alpha | is_digit,	/* A-Za-z0-9 */
							is_idnum = is_idst | is_digit,	/* A-Za-z0-9_ */
							is_graph = is_alnum | is_punct,	/* isprint and not space */
							is_cppsp = is_vsp | is_nvsp,	/* isspace + \0 */
							is_basic = is_print | is_cppsp     /* basic charset of ISO C  (plus ` and @)  */
	};


